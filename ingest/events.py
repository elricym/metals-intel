def load_events():
    conn = get_db()
    count = 0
    
    # FOMC decisions
    for date, actual, forecast, previous, notes in FOMC_DECISIONS:
        surprise = None
        if actual is not None and forecast is not None:
            surprise = round(actual - forecast, 4)
        conn.execute(
            "INSERT OR REPLACE INTO macro_events (date, event_type, actual, forecast, previous, surprise, notes) "
            "VALUES (?, 'FOMC', ?, ?, ?, ?, ?)",
            (date, actual, forecast, previous, surprise, notes)
        )
        count += 1
    
    conn.commit()
    conn.close()
    print(f"  Inserted {count} macro events")

if __name__ == '__main__':
    load_events()
